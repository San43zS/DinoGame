proc calculateCactusPosition, CactusXDD, CactusXDW
  locals
    MinCactusX dd 2.0
    MaxScreenX dd 310.0
  endl

  mov bx, [CactusXDD]

  mov eax, [MinCactusX]
  cmp [bx], eax
  jl .RespaunObj
  jmp .SkipRespaun
  .RespaunObj:
    movzx di, [CurrentCactusPos]
    shl di, 2
    fld dword[RandomCactusPos + di]
    fstp dword[bx]
    inc [CurrentCactusPos]
    mov al, [CactusPosCount]
    cmp [CurrentCactusPos], al
    jz .SetZero
    jmp @F
      .SetZero:
        mov [CurrentCactusPos], 0
    @@:
  .SkipRespaun:

  fld dword[bx]
  fsub dword[CactusSpeed]
  fstp dword[bx]
  fld dword[bx]
  mov si, [CactusXDW]
  fistp word[si]

  mov eax, [bx]
  cmp eax, [MaxScreenX]
  jg .HideCactus
  mov si, [CactusXDW]
  stdcall CollisionCheks, [si]
  jmp @F
    .HideCactus:
      mov word[si], 315
  @@:
  ret
endp


proc CollisionCheks, CactusXDW
